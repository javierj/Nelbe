package org.iwt2.nebel.model;

public class ParSnakeArrowButton {
	Snake s;
	ArrowButton ab;
	
	
	public ParSnakeArrowButton(Snake s, ArrowButton ab) {
		super();
		this.s = s;
		this.ab = ab;
	}
	public Snake getS() {
		return s;
	}
	public void setS(Snake s) {
		this.s = s;
	}
	public ArrowButton getAb() {
		return ab;
	}
	public void setAb(ArrowButton ab) {
		this.ab = ab;
	}
	
	

}
